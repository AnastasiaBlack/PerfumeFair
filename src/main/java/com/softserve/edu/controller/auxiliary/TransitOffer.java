package com.softserve.edu.controller.auxiliary;

public class TransitOffer {
    String perfumeName;
    String brandName;
    String price;
    String volumeForSale;

    public String getPerfumeName() {
        return perfumeName;
    }

    public void setPerfumeName(String perfumeName) {
        this.perfumeName = perfumeName;
    }

    public String getBrandName() {
        return brandName;
    }

    public void setBrandName(String brandName) {
        this.brandName = brandName;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getVolumeForSale() {
        return volumeForSale;
    }

    public void setVolumeForSale(String volumeForSale) {
        this.volumeForSale = volumeForSale;
    }
}
